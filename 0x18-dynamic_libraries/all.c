#include <stdio.h>

/**
* mod - two int
* @a: first int
* @b: second int
* Return: final answer
*/
int mod(int a, int b)
{
    return (a % b);
}
/**
* div - div two int
* @a: first int
* @b: second int
* Return: addition
*/
int div(int a, int b)
{
    return (a / b);
}
/**
* mul - multiply two int
* @a: first int
* @b: second int
* Return: addition
*/
int mul(int a, int b)
{
    return (a * b);
}
/**
* sub - sub two int
* @a: first int
* @b: second int
* Return: subtract
*/
int sub(int a, int b)
{
    return (a - b);
}
/**
* add - add two int
* @a: first int
* @b: second int
* Return: subtract
*/
int add(int a, int b)
{
    return (a + b);
}