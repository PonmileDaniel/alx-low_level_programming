#include "lists.h"

/**
 * reverse_listint - This reverse a linked list
 * @head: Head
 * Return: pointer to the firsst
 */
listint_t *reverse_listint(listint_t **head)
{
	listint_t *prev = NULL;
	listint_t *next = NULL;

	while (*head)
	{
		next = (*head)->next;
		(*head)->next = prev;
		prev = *head;
		*head = next;
	}
	*head = prev;

	return (*head);
}
