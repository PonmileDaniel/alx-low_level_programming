#include "3-calc.h"

/**
 * op_add - adds two number
 * @a: first number
 * @b: second number
 * Return: add
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Substraction of two number
 * @a: first number
 * @b: Second number
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplication of two number
 * @a: first number
 * @b: Second number
 * Return: Mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division of two number
 * @a: first number
 * @b: Second number
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - the remainder of the division of two number
 * @a: first number
 * @b: Second number
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
